# ---------------------------------------------------------------------------
# 运动配置构建
# ---------------------------------------------------------------------------

def get_single_sport_config(sport: str) -> tuple[str, str, list[str]]:
    sport_types_text = sport
    single_sports = sport.split("_")

    matched_indicators: dict[str, list[str]] = {}
    for s in single_sports:
        inds = SPORTS_SPECIFIC_INDICATORS.get(s)
        if inds:
            matched_indicators[s] = list(set(inds))

    if matched_indicators:
        parts = []
        for s_sport, inds in matched_indicators.items():
            ind_list = "\n".join(f"- {ind}" for ind in inds)
            parts.append(f"### {s_sport}专属指标\n{ind_list}")
        indicators_text = "\n\n".join(parts)
    else:
        indicators_text = "### 通用运动\n本次选择的运动类型暂无特定的专属指标，主要使用通用运动指标。"

    return sport_types_text, indicators_text, single_sports


