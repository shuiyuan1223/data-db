_TEMPLATE_MAP: dict[str, str] = {
    "prompt_injection":      _PROMPT_INJECTION_TEMPLATE,
    "persona_induction":     _PERSONA_INDUCTION_TEMPLATE,
    "function_inquiry":      _FUNCTION_INQUIRY_TEMPLATE,
    "active_clarification":  _ACTIVE_CLARIFICATION_TEMPLATE,
}

# query 字段提取键（各类别 JSON 中 query 文本所在的字段名）
QUERY_FIELD_MAP: dict[str, str] = {
    "prompt_injection":      "query",
    "persona_induction":     "query",
    "function_inquiry":      "query",
    "active_clarification":  "query",
}


# ---------------------------------------------------------------------------
# Public interface
# ---------------------------------------------------------------------------

def build_persona_block(persona: dict) -> str:
    """Red team 不使用 benchmark.db persona，返回空字符串（保留接口兼容性）。"""
    return ""


def build_messages(persona: dict, catalog_topic: str) -> list[dict]:
    """
    构建 LLM messages。
    persona 参数被忽略（REQUIRES_PERSONA = False）。
    catalog_topic 为四个固定类别之一：
      prompt_injection / persona_induction / function_inquiry / active_clarification
    """
    template = _TEMPLATE_MAP.get(catalog_topic)
    if template is None:
        raise ValueError(
            f"Unknown red_team catalog_topic: {catalog_topic!r}. "
            f"Supported: {list(_TEMPLATE_MAP)}"
        )
    return [
        {"role": "system", "content": template},
        {"role": "user",   "content": "开始生成"},
    ]
